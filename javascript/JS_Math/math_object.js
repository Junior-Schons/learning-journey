//Math.round() retorna o inteiro mais proximo.
console.log(Math.round(4.6));//console trara o numero 5!

//----------------
console.log('Console Math.ceil()--------------------------')
//Math.ceil(x) retorna o valor de x arredondado para cima o inteiro mais proximo :
console.log(Math.ceil(4.9));
console.log(Math.ceil(4.7));
console.log(Math.ceil(4.4));
console.log(Math.ceil(4.2));
console.log(Math.ceil(-4.2));
//----------------------
console.log('Console Math.floor()--------------------------')
//Math.floor(x) retorna o valor de x arredondado para baixo o inteiro mais proximo:
console.log(Math.floor(4.9));
console.log(Math.floor(4.4));
console.log(Math.floor(4.2));
console.log(Math.floor(-4.2));
//-------------------------------
console.log('Console Math.trunc()--------------------------')

//Math.trunc(x) retorna a parte inteira de x:
console.log(Math.trunc(4.7));

//------------------------------
console.log('Console Math.sign()--------------------------')

//Math.sign(x) retorna se x for negativo, nulo ou pasitivo.
console.log(Math.sign(-4));
console.log(Math.sign(0));
console.log(Math.sign(4));

//------------------------
console.log('Console Math.pow()--------------------------')

//Math.pow(x, y) retorna o valor de X elevado a Y:
console.log(Math.pow(8, 2));
console.log(Math.pow(7, 3));

//---------------------
console.log('Console Math.sqrt()--------------------------')

//Math.sqrt(x) retorna a raiz quadrada de X:
console.log(Math.sqrt(8));
console.log(Math.sqrt(64));

//---------------------
console.log('console Math.abs()--------------------------')

//Math.abs(x) retorna o valor absoluto (positivo) de X:
console.log(Math.abs(-4.7));


